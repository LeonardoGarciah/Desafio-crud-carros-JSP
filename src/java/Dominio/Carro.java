package Dominio;

/**
 *
 * @author leona
 */
public class Carro {
    private String marca;
    private String modelo;
    private String cor;
    private String nome;
    private int anoFab;
    private double preco;
    private int idCar;

    public Carro(int idCar,String marca, String modelo, String cor, String nome, int anoFab, double preco) {
        this.idCar = idCar;
        this.marca = marca;
        this.modelo = modelo;
        this.cor = cor;
        this.nome = nome;
        this.anoFab = anoFab;
        this.preco = preco;
    }

    public int getIdCar() {
        return idCar;
    }

    public void setIdCar(int idCar) {
        this.idCar = idCar;
    }
    
    
    
    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getAnoFab() {
        return anoFab;
    }

    public void setAnoFab(int anoFab) {
        this.anoFab = anoFab;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco(double preco) {
        this.preco = preco;
    }
    
    
}
